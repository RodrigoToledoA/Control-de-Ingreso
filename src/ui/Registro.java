    private void refreshTable() {
        
        //Levantamos los datos a cargar
        Usuario[] listado = ListaUsuarios.getLista();
        
        //Creamos el modelo de datos de la tabla.
        DefaultTableModel model = new DefaultTableModel() {
            /*Edito un m√©todo de la clase "DefaultTableModel" y cada vez que 
             se renderiza un celda, cuando se pregunta si es editable le digo que no.
             Lo explico mejor en clase;
             */
            @Override
            public boolean isCellEditable(int row, int column) {
                if (column==5){
                    return true;
                }
                return false;
            }
            Object[][] data = {};
            //
            // This method is used by the JTable to define the default
            // renderer or editor for each cell. For example if you have
            // a boolean data it will be rendered as a check box. A
            // number value is right aligned.
            
//            @Override
//            public Class<?> getColumnClass(int columnIndex) {
//            return getValueAt(0, columnIndex).getClass();
//            
//        }

        };

        model.addColumn("Nombre");
        model.addColumn("Dni");
        model.addColumn("Telefono");
        model.addColumn("Mail");
        model.addColumn("Ultimo ingreso");

        for (int i = 0; i < listado.length; i++) {
            if (listado[i] != null) {
                Object[] fila = new Object[5];
                fila[0] = listado[i].getNombre();
                fila[1] = "" + listado[i].getDni();
                fila[2] = "" + listado[i].getTel();
                fila[3] = listado[i].getMail();
                fila[4] = listado[i].gethLlegada();                
                model.addRow(fila);
            }
        }
        tblRegistro.setModel(model);
    }
